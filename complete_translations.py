def update_po_file(filename):
    """Update the PO file with Persian translations"""
    with open(filename, 'r', encoding='utf-8') as f:
        lines = f.readlines()
    
    # Create a map for quick lookup
    result_lines = []
    i = 0
    
    while i < len(lines):
        line = lines[i]
        result_lines.append(line)
        
        # Check if this is a msgid line followed by an empty msgstr
        if line.startswith('msgid "'):
            # Extract the msgid content
            msgid_content = line[len('msgid "'):].rstrip().rstrip('"')
            
            # Check if next non-empty line is an empty msgstr
            j = i + 1
            while j < len(lines) and lines[j].strip() == '':
                result_lines.append(lines[j])
                j += 1
            
            if j < len(lines) and lines[j].startswith('msgstr ""'):
                # Check if we have a translation for this
                if msgid_content in translations:
                    # Replace the empty msgstr with the translation
                    result_lines.append(f'msgstr "{translations[msgid_content]}"\n')
                    # Skip the original empty msgstr line
                    i = j + 1
                    continue
        
        i += 1
    
    # Write back
    with open(filename, 'w', encoding='utf-8') as f:
        f.writelines(result_lines)

